# -- linkcheck event listener ------------------------------------------------
def strip_github_anchor(app, url):
    if url and (match := re.match(r"(https:\/\/github\.com\/[\w\d._-]+\/[\w\d._-]+\/blob\/.*?)#L\d+(?:-L\d+)?", url)) != None:
        url = match.group(1)
    return url

def setup(app):
    app.connect('linkcheck-process-uri', strip_github_anchor)

